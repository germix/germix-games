#include <mach/Point.h>

#include <ctype.h>
#include <stdlib.h>

Point::Point(const char* s) : x(0), y(0)
{
	if(*s)
	{
		x = atoi(s);
		if(*s == '-') s++;
		while(isalnum(*s)) s++;		// Digitos
		s++;						// ','
		y = atoi(s);
	}
}

